package com.akm.messagescheduler.error;

public class InvalidReceiverNumberException extends Exception {

    public InvalidReceiverNumberException() {
        super();
    }

    public InvalidReceiverNumberException(java.lang.String message) {
        super(message);
    }

    public InvalidReceiverNumberException(java.lang.String message, java.lang.Throwable cause) {
        super(message, cause);
    }

    public InvalidReceiverNumberException(java.lang.Throwable cause) {
        super(cause);
    }

    protected InvalidReceiverNumberException(java.lang.String message, java.lang.Throwable cause,
            boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
